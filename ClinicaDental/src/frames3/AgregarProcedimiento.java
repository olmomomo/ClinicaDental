    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
        // TODO add your handling code here:
         try {
            int idServicio = Integer.parseInt(cmbServicio.getSelectedItem().toString().split(" - ")[0]);
            int idProcedimiento = Integer.parseInt(cmbProcedimiento.getSelectedItem().toString().split(" - ")[0]);
            String observaciones = txtObservaciones.getText();

            boolean exito = conexion.insertar(
                    "tratamiento",
                    "id_servicio, id_procedimiento, observaciones",
                    "?, ?, ?",
                    idServicio, idProcedimiento, observaciones
            );

            if (exito) {
                JOptionPane.showMessageDialog(this, "Tratamiento agregado correctamente.");
                if (listener != null) listener.run();
                dispose();
            } else {
                JOptionPane.showMessageDialog(this, "Error al agregar tratamiento.");
            }

        } catch (Exception e) {
            e.printStackTrace();
        }
    }//GEN-LAST:event_btnGuardarActionPerformed

