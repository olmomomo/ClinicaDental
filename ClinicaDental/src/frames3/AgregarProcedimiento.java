     private void cargarCombos() {
        try {
            // Servicios
            ResultSet rsServicios = conexion.consultarTodo("servicio");
            while (rsServicios.next()) {
                cmbServicio.addItem(rsServicios.getInt("id_servicio") + " - " + rsServicios.getString("nombre"));
            }
            rsServicios.close();

            // Procedimientos
            ResultSet rsProcedimientos = conexion.consultarTodo("procedimiento");
            while (rsProcedimientos.next()) {
                cmbProcedimiento.addItem(rsProcedimientos.getInt("id_procedimiento") + " - " + rsProcedimientos.getString("nombre"));
            }
            rsProcedimientos.close();

        } catch (Exception e) {
            e.printStackTrace();
        }
    }

